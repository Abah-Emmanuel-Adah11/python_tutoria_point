#!/usr/bin/python3

list = ['abcd' , 786 , 2.23 , 'john' , 70.2]

tinylist = [123, 'john']

print (list)  # print complete list

print (list[0]) # print the first element of the list

print (list[1:3]) # print element starting from 2nd till 3rd

print (list[2:]) # print element starting from 3rd element

print (tinylist * 2) # print list two times

print (list + tinylist) # prints concatenated lists
