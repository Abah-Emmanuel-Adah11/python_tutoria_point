#!/usr/bin/python3

str = 'Hello World!'

print (str) # print the complete string

print (str[0]) # print first character of the string

print (str[2:5]) # print characters stating from 3rd to 5th

print (str[2:]) # print string starting from 3rd character

print (str * 2) # print string two times

print (str + "Test") # print concatenated string
